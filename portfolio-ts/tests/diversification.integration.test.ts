import { describe, expect, test } from "bun:test";
import { SQL } from "bun";

const DB_URL = process.env.PORTFOLIO_DB_URL;
const MAYBE_SKIP = DB_URL ? describe : describe.skip;

function tempTableDDL() {
  return {
    transactions: `
      CREATE TEMP TABLE transactions (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        date DATE NOT NULL,
        asset VARCHAR NOT NULL,
        action VARCHAR NOT NULL,
        quantity DOUBLE PRECISION NOT NULL,
        asset_type VARCHAR,
        price DOUBLE PRECISION,
        currency VARCHAR,
        fees DOUBLE PRECISION,
        fee_currency VARCHAR,
        exchange VARCHAR,
        data_source VARCHAR,
        account VARCHAR,
        exchange_group_id TEXT,
        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
        updated_at TIMESTAMP
      ) ON COMMIT DROP
    `,
    prices: `
      CREATE TEMP TABLE prices (
        date DATE NOT NULL,
        ticker VARCHAR NOT NULL,
        price DOUBLE PRECISION NOT NULL,
        PRIMARY KEY (date, ticker)
      ) ON COMMIT DROP
    `,
  };
}

// ── Fixture A: two perfectly-correlated assets ──
// AAPL: 50→55→60.5→66.55  (% returns: 0.1, 0.1, 0.1)
// GOOGL: 100→110→121→133.1 (% returns: 0.1, 0.1, 0.1)
// Equal dollar allocation → w_A = w_B = 0.5.
// HHI = (0.5² + 0.5²) × 10000 = 5000
// effective_holdings = 1 / 0.5 = 2.0
// ρ = 1.0
// CWHHI = (0.25 + 0.25 + 2×0.5×0.5×1.0) × 10000 = 10000
function fixturePerfectCorr(): { tx: string; prices: string; asOf: string } {
  return {
    tx: `
      INSERT INTO transactions (date, asset, action, quantity, price, fees, currency, exchange)
      VALUES
        ('2026-01-01', 'USD',   'DEPOSIT', 1000, NULL, 0, 'USD', 'bank'),
        ('2026-01-01', 'AAPL',  'BUY',     10,   50,   0, 'USD', 'IBKR'),
        ('2026-01-01', 'GOOGL', 'BUY',     5,    100,  0, 'USD', 'IBKR')
    `,
    prices: `
      INSERT INTO prices (ticker, date, price)
      VALUES
        ('AAPL',  '2026-01-01', 50.0),
        ('AAPL',  '2026-01-02', 55.0),
        ('AAPL',  '2026-01-03', 60.5),
        ('AAPL',  '2026-01-04', 66.55),
        ('GOOGL', '2026-01-01', 100.0),
        ('GOOGL', '2026-01-02', 110.0),
        ('GOOGL', '2026-01-03', 121.0),
        ('GOOGL', '2026-01-04', 133.1)
    `,
    asOf: "2026-01-04",
  };
}

// ── Fixture B: perfectly negative correlation ──
// TESTA: 100→110→99→108.9  (% returns: 0.1, -0.1, 0.1)
// TESTB: 200→180→198→178.2 (% returns: -0.1, 0.1, -0.1)
// Weights use as-of MARKET value (portfolio_allocation_sql), so quantities are
// chosen so the two positions end at EQUAL value (1.8×108.9 == 1.1×178.2 == 196.02)
// and the deposit is fully invested (no leftover cash holding). Only then do the
// weights equal 0.5/0.5 and CWHHI collapse to 0 for a perfectly hedged pair.
// HHI = 5000 ; effective_holdings = 2.0
// CWHHI = (0.25 + 0.25 + 2×0.5×0.5×(-1.0)) × 10000 = 0
function fixtureNegCorr(): { tx: string; prices: string; asOf: string } {
  return {
    tx: `
      INSERT INTO transactions (date, asset, action, quantity, price, fees, currency, exchange)
      VALUES
        ('2026-01-01', 'USD',   'DEPOSIT', 400, NULL, 0, 'USD', 'bank'),
        ('2026-01-01', 'TESTA', 'BUY',     1.8,  100,  0, 'USD', 'IBKR'),
        ('2026-01-01', 'TESTB', 'BUY',     1.1,  200,  0, 'USD', 'IBKR')
    `,
    prices: `
      INSERT INTO prices (ticker, date, price)
      VALUES
        ('TESTA', '2026-01-01', 100.0),
        ('TESTA', '2026-01-02', 110.0),
        ('TESTA', '2026-01-03', 99.0),
        ('TESTA', '2026-01-04', 108.9),
        ('TESTB', '2026-01-01', 200.0),
        ('TESTB', '2026-01-02', 180.0),
        ('TESTB', '2026-01-03', 198.0),
        ('TESTB', '2026-01-04', 178.2)
    `,
    asOf: "2026-01-04",
  };
}

// ── Fixture C: zero cross-correlation ──
