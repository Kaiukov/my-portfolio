// ── Fixture F: p_min_correlation filter ──
// Same as Fixture A (ρ=1.0) but filter at 0.9: pair passes.
// Then filter at 1.1: no pairs pass → avg/max/min = NULL.
function fixtureMinCorr(): { tx: string; prices: string; asOf: string } {
  return fixturePerfectCorr();
}

MAYBE_SKIP("integration: diversification_depth_sql", () => {
  const ddl = tempTableDDL();

  test("two perfectly-correlated assets: ρ=1, HHI=5000, eff=2, CWHHI=10000", async () => {
    const sql = new SQL(DB_URL!, { max: 1 });
    try {
      await sql.begin(async (tx: any) => {
        await tx.unsafe(ddl.transactions);
        await tx.unsafe(ddl.prices);

        const f = fixturePerfectCorr();
        await tx.unsafe(f.tx);
        await tx.unsafe(f.prices);

        const [row] = await tx.unsafe(
          `SELECT * FROM portfolio_diversification_depth_sql($1)`,
          [f.asOf],
        ) as any[];

        expect(row.as_of_date).toBe(f.asOf);

        // HHI: 0.5² + 0.5² = 0.5 → ×10000 = 5000
        expect(Number(row.hhi)).toBeCloseTo(5000, 1);
        expect(Number(row.effective_holdings)).toBeCloseTo(2.0, 3);

        // ρ = 1.0 for the single pair
        expect(Number(row.avg_pairwise_correlation)).toBeCloseTo(1.0, 4);
        expect(Number(row.max_pairwise_correlation)).toBeCloseTo(1.0, 4);
        expect(Number(row.min_pairwise_correlation)).toBeCloseTo(1.0, 4);

        // CWHHI = (0.25 + 0.25 + 2×0.5×0.5×1.0) × 10000 = 10000
        expect(Number(row.correlation_weighted_hhi)).toBeCloseTo(10000, 1);

        expect(Number(row.total_holdings)).toBeGreaterThanOrEqual(2);
      });
    } finally {
      await sql.end();
    }
  });

  test("perfectly negative correlation: ρ=-1, CWHHI=0", async () => {
    const sql = new SQL(DB_URL!, { max: 1 });
    try {
      await sql.begin(async (tx: any) => {
        await tx.unsafe(ddl.transactions);
        await tx.unsafe(ddl.prices);

        const f = fixtureNegCorr();
        await tx.unsafe(f.tx);
        await tx.unsafe(f.prices);

        const [row] = await tx.unsafe(
          `SELECT * FROM portfolio_diversification_depth_sql($1)`,
          [f.asOf],
        ) as any[];

        expect(Number(row.hhi)).toBeCloseTo(5000, 1);
        expect(Number(row.avg_pairwise_correlation)).toBeCloseTo(-1.0, 4);
        expect(Number(row.min_pairwise_correlation)).toBeCloseTo(-1.0, 4);

        // CWHHI with ρ=-1: perfectly diversifying → weighted HHI = 0
        expect(Number(row.correlation_weighted_hhi)).toBeCloseTo(0, 1);
      });
    } finally {
      await sql.end();
    }
  });

  test("uncorrelated pair: ρ≈0, CWHHI≈HHI", async () => {
    const sql = new SQL(DB_URL!, { max: 1 });
    try {
      await sql.begin(async (tx: any) => {
        await tx.unsafe(ddl.transactions);
        await tx.unsafe(ddl.prices);

        const f = fixtureZeroCorr();
        await tx.unsafe(f.tx);
        await tx.unsafe(f.prices);

        const [row] = await tx.unsafe(
          `SELECT * FROM portfolio_diversification_depth_sql($1)`,
          [f.asOf],
        ) as any[];

        expect(Number(row.avg_pairwise_correlation)).toBeCloseTo(0, 3);

        // With ρ≈0: CWHHI ≈ HHI (no correlation penalty or benefit)
        expect(Number(row.correlation_weighted_hhi)).toBeCloseTo(
          Number(row.hhi),
          1,
        );
      });
    } finally {
      await sql.end();
    }
  });

  test("single holding: NULL correlations, CWHHI=HHI=10000", async () => {
    const sql = new SQL(DB_URL!, { max: 1 });
    try {
      await sql.begin(async (tx: any) => {
        await tx.unsafe(ddl.transactions);
        await tx.unsafe(ddl.prices);

        const f = fixtureSingle();
        await tx.unsafe(f.tx);
        await tx.unsafe(f.prices);

        const [row] = await tx.unsafe(
          `SELECT * FROM portfolio_diversification_depth_sql($1)`,
          [f.asOf],
        ) as any[];

        // Single holding + cash: one priced asset → <2 → correlations NULL
        expect(row.avg_pairwise_correlation).toBeNull();
        expect(row.max_pairwise_correlation).toBeNull();
        expect(row.min_pairwise_correlation).toBeNull();

        // CWHHI should reduce to HHI when no pairs available
        expect(Number(row.correlation_weighted_hhi)).toBeCloseTo(
          Number(row.hhi),
          1,
        );
      });
    } finally {
      await sql.end();
    }
  });

  test("cash excluded from correlation but included in HHI", async () => {
    const sql = new SQL(DB_URL!, { max: 1 });
    try {
      await sql.begin(async (tx: any) => {
        await tx.unsafe(ddl.transactions);
        await tx.unsafe(ddl.prices);

        const f = fixtureCashExcluded();
        await tx.unsafe(f.tx);
        await tx.unsafe(f.prices);

        const [row] = await tx.unsafe(
          `SELECT * FROM portfolio_diversification_depth_sql($1)`,
          [f.asOf],
        ) as any[];

        // Two priced assets → should have non-NULL correlations
        expect(row.avg_pairwise_correlation).not.toBeNull();

        // total_holdings includes cash (3 allocations: AAPL, GOOGL, USD)
        expect(Number(row.total_holdings)).toBeGreaterThanOrEqual(2);

        // HHI < 10000 because cash dilutes concentration
        expect(Number(row.hhi)).toBeLessThan(10000);
      });
    } finally {
      await sql.end();
    }
  });

  test("p_min_correlation filter excludes weak pairs", async () => {
    const sql = new SQL(DB_URL!, { max: 1 });
    try {
      await sql.begin(async (tx: any) => {
        await tx.unsafe(ddl.transactions);
        await tx.unsafe(ddl.prices);

        const f = fixtureMinCorr();
        await tx.unsafe(f.tx);
        await tx.unsafe(f.prices);

        // With p_min_correlation=0.9: ρ=1.0 pair passes
        const [row1] = await tx.unsafe(
          `SELECT * FROM portfolio_diversification_depth_sql($1, $2, $3)`,
          [f.asOf, 252, 0.9],
        ) as any[];

        expect(row1.avg_pairwise_correlation).not.toBeNull();
        expect(Number(row1.avg_pairwise_correlation)).toBeCloseTo(1.0, 4);

        // With p_min_correlation=1.5: no pair passes → correlations NULL
        const [row2] = await tx.unsafe(
          `SELECT * FROM portfolio_diversification_depth_sql($1, $2, $3)`,
          [f.asOf, 252, 1.5],
        ) as any[];

        expect(row2.avg_pairwise_correlation).toBeNull();
        expect(row2.max_pairwise_correlation).toBeNull();
        expect(row2.min_pairwise_correlation).toBeNull();

        // CWHHI still computed using all pairs (not filtered), so it stays
        expect(Number(row2.correlation_weighted_hhi)).toBeCloseTo(10000, 1);
      });
    } finally {
      await sql.end();
    }
  });

  test("lookback_days restricts the window", async () => {
    const sql = new SQL(DB_URL!, { max: 1 });
    try {
      await sql.begin(async (tx: any) => {
        await tx.unsafe(ddl.transactions);
        await tx.unsafe(ddl.prices);

        const f = fixturePerfectCorr();
        await tx.unsafe(f.tx);
        await tx.unsafe(f.prices);

        // Window=1 day: only 1 price point per asset → not enough for returns (<2) → no pairs
        const [row] = await tx.unsafe(
          `SELECT * FROM portfolio_diversification_depth_sql($1, $2, $3)`,
          [f.asOf, 1, 0.0],
        ) as any[];

        // Not enough priced assets (<2 returns needed) → correlations NULL
        expect(row.avg_pairwise_correlation).toBeNull();
      });
    } finally {
      await sql.end();
    }
  });
});
