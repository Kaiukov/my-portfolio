  test(
    "multi-BUY, multi-SELL FIFO: oldest lot first, correct holding_days, per-lot detail",
    async () => {
      const sql = new SQL(DB_URL!, { max: 1 });
      try {
        await sql.begin(async (tx: any) => {
          await tx.unsafe(`
            CREATE TEMP TABLE transactions (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              date DATE NOT NULL,
              asset VARCHAR NOT NULL,
              action VARCHAR NOT NULL,
              quantity DOUBLE PRECISION NOT NULL,
              asset_type VARCHAR,
              price DOUBLE PRECISION,
              currency VARCHAR,
              fees DOUBLE PRECISION,
              fee_currency VARCHAR,
              exchange VARCHAR,
              data_source VARCHAR,
              account VARCHAR,
              exchange_group_id TEXT,
              created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
              updated_at TIMESTAMP
            ) ON COMMIT DROP
          `);

          await tx.unsafe(`
            CREATE TEMP TABLE prices (
              date DATE NOT NULL,
              ticker VARCHAR NOT NULL,
              price DOUBLE PRECISION NOT NULL,
              PRIMARY KEY (date, ticker)
            ) ON COMMIT DROP
          `);

          await tx.unsafe(`
            INSERT INTO transactions (date, asset, action, quantity, price, fees, currency, fee_currency)
            VALUES
              ('2026-01-01', 'USD', 'DEPOSIT', 10000, NULL, 0, 'USD', NULL),
              ('2026-01-02', 'AAPL', 'BUY', 10, 150, 0, 'USD', NULL),
              ('2026-01-05', 'AAPL', 'BUY', 10, 110, 0, 'USD', NULL),
              ('2026-01-10', 'AAPL', 'SELL', 15, 120, 0, 'USD', NULL)
          `);

          await tx.unsafe(`
            INSERT INTO prices (ticker, date, price)
            VALUES
              ('AAPL', '2026-01-01', 150),
              ('AAPL', '2026-01-02', 150),
              ('AAPL', '2026-01-05', 110),
              ('AAPL', '2026-01-10', 120)
          `);

          const asOf = "2026-01-10";

          const detailRows = await tx.unsafe(
            `SELECT * FROM portfolio_realized_gains_sql(NULL, $1, NULL) ORDER BY sell_date, matched_buy_date`,
            [asOf],
          ) as any[];

          // Hand-calculated FIFO:
          // SELL 15 @ $120, proceeds = 15 * 120 = 1800
          // Lot1 (oldest): 10 AAPL @ $150 → consume 10
          //   cost_consumed = 10 * 150 = 1500
          //   proceeds_share = 1800 * (10 / 15) = 1200
          //   realized = 1200 - 1500 = -300
          //   holding_days = 8 (Jan 2 → Jan 10)
          //
          // Lot2: 10 AAPL @ $110 → consume 5
          //   cost_consumed = 5 * 110 = 550
          //   proceeds_share = 1800 * (5 / 15) = 600
          //   realized = 600 - 550 = +50
          //   holding_days = 5 (Jan 5 → Jan 10)
          //
          // Total realized = -250

          expect(detailRows.length).toBe(2);

          const [row1, row2] = detailRows;
          expect(row1.asset).toBe("AAPL");
          expect(Number(row1.sell_quantity)).toBeCloseTo(10, 6);
          expect(Number(row1.proceeds_usd)).toBeCloseTo(1200, 2);
          expect(Number(row1.cost_basis_usd)).toBeCloseTo(1500, 2);
          expect(Number(row1.realized_gain)).toBeCloseTo(-300, 2);
          expect(Number(row1.holding_days)).toBe(8);

          expect(Number(row2.sell_quantity)).toBeCloseTo(5, 6);
          expect(Number(row2.proceeds_usd)).toBeCloseTo(600, 2);
          expect(Number(row2.cost_basis_usd)).toBeCloseTo(550, 2);
          expect(Number(row2.realized_gain)).toBeCloseTo(50, 2);
          expect(Number(row2.holding_days)).toBe(5);

          // FIFO: oldest first → row1 buy_id < row2 buy_id
          expect(Number(row1.matched_buy_id)).toBeLessThan(Number(row2.matched_buy_id));
          expect(row1.matched_buy_date).toBe("2026-01-02");
          expect(row2.matched_buy_date).toBe("2026-01-05");
        });
      } finally {
        await sql.end();
      }
    },
    { timeout: 15000 },
  );

