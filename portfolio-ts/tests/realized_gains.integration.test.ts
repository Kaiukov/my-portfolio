          // Expected: 1 match row
          // BUY 100 @ 150 → cost 15000, unit_cost 150
          // SPLIT 4 → remaining 400 @ unit_cost 37.50 (150/4)
          // SELL 100 @ 50 → proceeds 5000, cost 100*37.50 = 3750, realized = 1250
          // holding_days = 2 (Jun 2 → Jun 4)

          expect(detailRows.length).toBe(1);
          expect(detailRows[0].asset).toBe("AAPL");
          expect(Number(detailRows[0].sell_quantity)).toBeCloseTo(100, 6);
          expect(Number(detailRows[0].proceeds_usd)).toBeCloseTo(5000, 2);
          expect(Number(detailRows[0].cost_basis_usd)).toBeCloseTo(3750, 2);
          expect(Number(detailRows[0].realized_gain)).toBeCloseTo(1250, 2);
          expect(Number(detailRows[0].holding_days)).toBe(2);

          // SPLIT should not produce any realized-gain rows (no taxable event)
          const splitOnly = await tx.unsafe(
            `SELECT * FROM portfolio_realized_gains_sql(NULL, '2026-06-03', NULL)`,
          ) as any[];
          expect(splitOnly.length).toBe(0);

